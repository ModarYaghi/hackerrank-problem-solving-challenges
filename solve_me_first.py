"""Hacker Rank Problem Solving - Challenge 01; Solve Me First"""

# This challenge isn't explained well at the hackerrank site,
# but I think this solution is the simplest one to get the challenge passed.


def solve_me_first(a, b):
    return a + b


a = int(input())
b = int(input())

print(solve_me_first(a, b))


